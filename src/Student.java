public class Student {
    private String name;
    private int age;

    public Student(String name, int age) {
        this.name = name;
        this.age = age;
    }
    /*
    method for getting the name of the student
    */
    public String getName() {
        return name;
    }
    /*
    method for getting the age of the student
    */
    public int getAge() {
        return age;
    }
}
